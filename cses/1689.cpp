#include <bits/stdc++.h>
using namespace std;

int A[8][8] = {
    {0, 51, 12, 33,   2, 53, 10, 23},
    {13, 32,  1, 52, 11, 22, 55,  8},
    {50, 63, 34,  3, 54,  9, 24, 21},
    {31, 14, 41, 38, 35,  4,  7, 56},
    {62, 49, 36,  5, 40, 43, 20, 25},
    {15, 30, 39, 42, 37,  6, 57, 44},
    {48, 61, 28, 17, 46, 59, 26, 19},
    {29, 16, 47, 60, 27, 18, 45, 58},
};

int main() {
    int N = 8;
    int i, j; cin >> j >> i; --i, --j;
    int d = A[i][j];
    for (int r = 0; r < N; ++r) {
        for (int c = 0; c < N; ++c) {
            cout << (A[r][c]-d+N*N)%(N*N)+1 << ' ';
        }
        cout << endl;
    }
}